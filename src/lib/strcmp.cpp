#include "types.h"

namespace lib {

i32 strcmp(const i8* s1, const i8* s2) noexcept {
    while (*s1 && *s1 == *s2) {
        s1++;
        s2++;
    }
    return *(u8*)s1 - *(u8*)s2;
}

}  // namespace lib
